public class Variables_2 {
    public static void main(String[] args) {
        int age; // this is variable declaration.
        age=22; //This is a variable assignment. When assignment is done at declaration its called initialization.
        double bodyFat= 10.4;  // thiis is a variable initialization.
        System.out.println("My age is: " + age);
        System.out.println("Your body fat is: " + bodyFat);
    }
    
}
